#include <bits/stdc++.h>

typedef long long ll;

using namespace std;

int main() {

  ios_base::sync_with_stdio(false);
  cin.tie(NULL);

  ll k;
  cin >> k;
  cout << 2 << " " << 3 << "\n";
  cout << ((1<<17)+ k) << " " << (1<<17) << " " << 0 << "\n";
  cout << (k) << " " << ((1<<17) + k) << " " << k << "\n";
  return 0;
}


